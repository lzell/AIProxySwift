    // MARK: - Filters

    // ComparisonFilter
    public struct ComparisonFilter: Decodable {
        public let key: String
        public let type: ComparisonOperator
        public let value: AIProxyJSONValue

        public enum ComparisonOperator: String, Decodable {
            case eq, ne, gt, gte, lt, lte
        }
    }

    // CompoundFilter – a recursive filter combining an array of filters.
    public struct CompoundFilter: Decodable {
        public let filters: [Filter]
        public let type: CompoundFilterType

        public enum CompoundFilterType: String, Decodable {
            case and, or
        }
    }

    // Filter union: either a ComparisonFilter or a CompoundFilter.
    public enum Filter: Decodable {
        case comparison(ComparisonFilter)
        case compound(CompoundFilter)

        private enum CodingKeys: String, CodingKey {
            case type
        }

        public init(from decoder: Decoder) throws {
            let container = try decoder.container(keyedBy: CodingKeys.self)
            let typeValue = try container.decode(String.self, forKey: .type)
            if typeValue == "and" || typeValue == "or" {
                self = .compound(try CompoundFilter(from: decoder))
            } else if ["eq", "ne", "gt", "gte", "lt", "lte"].contains(typeValue) {
                self = .comparison(try ComparisonFilter(from: decoder))
            } else {
                throw DecodingError.dataCorruptedError(forKey: .type,
                    in: container,
                    debugDescription: "Unknown filter type: \(typeValue)")
            }
        }
    }

