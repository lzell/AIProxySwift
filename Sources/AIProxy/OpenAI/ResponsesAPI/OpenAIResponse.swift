    // MARK: - Tool (Union)

    /// The union type representing one of:
    /// - FileSearchTool
    /// - FunctionTool
    /// - ComputerTool
    /// - WebSearchTool
    public enum Tool: Decodable {
        case fileSearch(FileSearchTool)
        case function(FunctionTool)
        case computer(ComputerTool)
        case webSearch(WebSearchTool)

        enum CodingKeys: String, CodingKey {
            case type
        }

        public init(from decoder: Decoder) throws {
            let container = try decoder.container(keyedBy: CodingKeys.self)
            let typeValue = try container.decode(String.self, forKey: .type)
            switch typeValue {
            case "file_search":
                self = .fileSearch(try FileSearchTool(from: decoder))
            case "function":
                self = .function(try FunctionTool(from: decoder))
            case "computer_use_preview":
                self = .computer(try ComputerTool(from: decoder))
            case "web_search_preview", "web_search_preview_2025_03_11":
                self = .webSearch(try WebSearchTool(from: decoder))
            default:
                throw DecodingError.dataCorruptedError(forKey: .type,
                                                        in: container,
                                                        debugDescription: "Unknown tool type: \(typeValue)")
            }
        }
    }


