/// A tool representing a request to execute one or more shell commands.
nonisolated public struct OpenAIShellToolCall: Encodable, Sendable {
    /// The shell commands and limits that describe how to run the tool call.
    public let action: OpenAIShellToolCallAction

    /// The unique ID of the shell tool call generated by the model.
    public let callID: String

    /// The type of the item. Always `shell_call`.
    public let type = "shell_call"

    /// The unique ID of the shell tool call. Populated when this item is returned via API.
    public let id: String?

    /// The status of the shell call.
    ///
    /// One of `in_progress`, `completed`, or `incomplete`.
    public let status: Status?

    /// Creates a new shell tool call item parameter.
    /// - Parameters:
    ///   - action: The shell commands and limits that describe how to run the tool call.
    ///   - callID: The unique ID of the shell tool call generated by the model.
    ///   - id: The unique ID of the shell tool call.
    ///   - status: The status of the shell call.
    public init(
        action: OpenAIShellToolCallAction,
        callID: String,
        id: String? = nil,
        status: Status? = nil
    ) {
        self.action = action
        self.callID = callID
        self.id = id
        self.status = status
    }

    private enum CodingKeys: String, CodingKey {
        case action
        case callID = "call_id"
        case type
        case id
        case status
    }

    public func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(action, forKey: .action)
        try container.encode(callID, forKey: .callID)
        try container.encode(type, forKey: .type)
        try container.encodeIfPresent(id, forKey: .id)
        try container.encodeIfPresent(status, forKey: .status)
    }
}

extension OpenAIShellToolCall {
    /// The status of the shell call.
    public enum Status: String, Encodable, Sendable {
        case inProgress = "in_progress"
        case completed
        case incomplete
    }
}
